package kr.co.himedia.opreator;

public class PrePostOperatorTest {
	
	public static void main(String[] args) {
		
		int x = 10;
		System.out.println(--x);  //9		
		System.out.println(x++);  //9
		System.out.println(x--);  //10
		System.out.println(++x);  //10
		System.out.println(x);
		
		
		
		
		
		
	}

}
